<script>
import { KeyCode } from '@/constants'

export default {
  functional: true,
  render( createElement, { listeners, children } ) {
    const click = e => {
      e.target.blur();
      if ( listeners.click != null )
        listeners.click();
    };
    const keydown = e => {
      if ( e.keyCode == KeyCode.Enter )
        click( e );
    };
    return createElement( 'a', { attrs: { role: "link", tabindex: "0" }, on: { click, keydown } }, children );
  }
}
</script>
